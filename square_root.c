int		square_root(float a)
{
	float	b;
	float	c;

	a = 4 * a;
	b = 1;
	while (a >= 2)
	{
		a = a / 4;
		b = 2 * b;
	}
	while (a < 1 / 2)
	{
		a = 4 * a;
		b = b / 2;
	}
	c = 1 - a;
	a = a * (1 + (c / 2));
	c = ((3 + c) * c * c) / 4;
	while (c >= 1E-15)
	{
		a = a * (1 + (c / 2));
		c = ((3 + c) * c * c) / 4;
	}
	return ((int)((a * b) + 0.999999999999999));
}
