int		square_root(int a)
{
	if (a == 1)
		return (2);
	if (a == 2)
		return (3);
	if (a >= 3 & a <= 4)
		return (4);
	if (a >= 5 & a <= 6)
		return (5);
	if (a >= 7 & a <= 9)
		return (6);
	if (a >= 10 & a <= 12)
		return (7);
	if (a >= 13 & a <= 16)
		return (8);
	if (a >= 17 & a <= 20)
		return (9);
	if (a >= 21 & a <= 25)
		return (10);
	if (a == 26)
		return (11);
	return (0);
}
